package com.castify.tv.models;

public class AdModel {
    private String vastURL;

    private String preRoll;

    private String midRoll;

    private String postRoll;

    private String adPod;

    private String vastID;

    private String adInterval;


    public String getVastURL() {
        return vastURL;
    }

    public void setVastURL(String vastURL) {
        this.vastURL = vastURL;
    }

    public String getPreRoll() {
        return preRoll;
    }

    public void setPreRoll(String preRoll) {
        this.preRoll = preRoll;
    }

    public String getMidRoll() {
        return midRoll;
    }

    public void setMidRoll(String midRoll) {
        this.midRoll = midRoll;
    }

    public String getPostRoll() {
        return postRoll;
    }

    public void setPostRoll(String postRoll) {
        this.postRoll = postRoll;
    }

    public String getAdPod() {
        return adPod;
    }

    public void setAdPod(String adPod) {
        this.adPod = adPod;
    }

    public String getVastID() {
        return vastID;
    }

    public void setVastID(String vastID) {
        this.vastID = vastID;
    }

    public String getAdInterval() {
        return adInterval;
    }

    public void setAdInterval(String adInterval) {
        this.adInterval = adInterval;
    }
}
