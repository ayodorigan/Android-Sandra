package com.castify.tv.models;

public class Info {
    private String output_version;

    private String output_type;

    private String last_build;

    private String appCategories;

    private String pub_id;

    private String app_bundle;

    private String hash;

    private String app_id;


    public String getOutput_version() {
        return output_version;
    }

    public void setOutput_version(String output_version) {
        this.output_version = output_version;
    }

    public String getOutput_type() {
        return output_type;
    }

    public void setOutput_type(String output_type) {
        this.output_type = output_type;
    }

    public String getLast_build() {
        return last_build;
    }

    public void setLast_build(String last_build) {
        this.last_build = last_build;
    }

    public String getAppCategories() {
        return appCategories;
    }

    public void setAppCategories(String appCategories) {
        this.appCategories = appCategories;
    }

    public String getPub_id() {
        return pub_id;
    }

    public void setPub_id(String pub_id) {
        this.pub_id = pub_id;
    }

    public String getApp_bundle() {
        return app_bundle;
    }

    public void setApp_bundle(String app_bundle) {
        this.app_bundle = app_bundle;
    }

    public String getHash() {
        return hash;
    }

    public void setHash(String hash) {
        this.hash = hash;
    }

    public String getApp_id() {
        return app_id;
    }

    public void setApp_id(String app_id) {
        this.app_id = app_id;
    }
}
